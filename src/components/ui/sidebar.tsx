const SIDEBAR_WIDTH_ICON = "3rem"
const SIDEBAR_KEYBOARD_SHORTCUT = "b"

type SidebarContext = {
  state: "expanded" | "collapsed" // This might be less relevant if always a sheet
  open: boolean // For desktop sheet state
  setOpen: (open: boolean) => void
  openMobile: boolean // For mobile sheet state
  setOpenMobile: (open: boolean) => void
  isMobile: boolean
  toggleSidebar: () => void // Will now primarily control the sheet
}

const SidebarContext = React.createContext<SidebarContext | null>(null)

function useSidebar() {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }

  return context
}

// SidebarProvider might be simplified or removed if Header manages its own sheet.
// For now, keeping it for potential context use if needed, but its role is diminished.
const SidebarProvider = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & {
    defaultOpen?: boolean
    open?: boolean
    onOpenChange?: (open: boolean) => void
  }
>(
  (
    {
      defaultOpen = false, // Default to closed for a sheet-based approach
      open: openProp,
      onOpenChange: setOpenProp,
      className,
      style,
      children,
      ...props
    },
    ref
  ) => {
    const isMobile = useIsMobile()
    const [openSheet, setOpenSheet] = React.useState(defaultOpen) // Unified sheet state

    // Allow prop control
    const currentOpen = openProp ?? openSheet;
    const setCurrentOpen = setOpenProp ?? setOpenSheet;
    
    const toggleSidebar = React.useCallback(() => {
      setCurrentOpen((prevOpen) => !prevOpen);
    }, [setCurrentOpen]);

    React.useEffect(() => {
      const handleKeyDown = (event: KeyboardEvent) => {
        if (
          event.key === SIDEBAR_KEYBOARD_SHORTCUT &&
          (event.metaKey || event.ctrlKey)
        ) {
          event.preventDefault()
          toggleSidebar()
        }
      }

      window.addEventListener("keydown", handleKeyDown)
      return () => window.removeEventListener("keydown", handleKeyDown)
    }, [toggleSidebar])

    // 'state' is less relevant for a pure sheet model but kept for compatibility for now
    const state = currentOpen ? "expanded" : "collapsed" 

    const contextValue = React.useMemo<SidebarContext>(
      () => ({
        state,
        open: currentOpen, // Desktop sheet state
        setOpen: setCurrentOpen,
        openMobile: currentOpen, // Mobile also uses the same sheet state
        setOpenMobile: setCurrentOpen,
        isMobile,
        toggleSidebar,
      }),
      [state, currentOpen, setCurrentOpen, isMobile, toggleSidebar]
    )

    return (
      <SidebarContext.Provider value={contextValue}>
        <TooltipProvider delayDuration={0}>
          <div
            style={
              {
                "--sidebar-width": SIDEBAR_WIDTH_MOBILE, // Use mobile width for the sheet
                ...style,
              } as React.CSSProperties
            }
            className={cn(
              "group/sidebar-wrapper flex min-h-svh w-full", // Removed inset bg
              className
            )}
            ref={ref}
            {...props}
          >
            {children} {/* children will now be Header and main content */}
          </div>
        </TooltipProvider>
      </SidebarContext.Provider>
    )
  }
)
SidebarProvider.displayName = "SidebarProvider"


